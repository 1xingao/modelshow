<script>
import { uploadStratumData } from '@/utils/api'

export default {
    name: 'BoreholeUpload',
    data() {
        return {
            selectedFile: null,
            isDragOver: false,
            uploading: false,
            uploadProgress: 0,
            uploadMessage: '',
            uploadStatus: '', // success, error, warning
            uploadResult: null, // 上传成功后的结果信息

        }
    },
    methods: {
        handleDragEnter() {
            this.isDragOver = true
        },

        handleDragOver() {
            this.isDragOver = true
        },

        handleDragLeave() {
            this.isDragOver = false
        },

        handleDrop(e) {
            this.isDragOver = false
            const files = e.dataTransfer.files
            if (files.length > 0) {
                this.selectFile(files[0])
            }
        },

        handleFileSelect(e) {
            const file = e.target.files[0]
            if (file) {
                this.selectFile(file)
            }
        },

        selectFile(file) {
            // 验证文件类型
            const allowedTypes = ['.txt', '.xlsx', '.xls', '.csv']
            const fileExt = '.' + file.name.split('.').pop().toLowerCase()
            
            if (!allowedTypes.includes(fileExt)) {
                this.showMessage('请选择 TXT、Excel 或 CSV 格式的文件', 'error')
                return
            }

            // 验证文件大小 (50MB)
            const maxSize = 50 * 1024 * 1024
            if (file.size > maxSize) {
                this.showMessage('文件大小不能超过 50MB', 'error')
                return
            }

            this.selectedFile = file
            this.clearMessages()
            
            // 重置文件输入框
            this.$refs.fileInput.value = ''
        },

        removeFile() {
            this.selectedFile = null
            this.previewData = null
            this.previewColumns = []
            this.dataFormat = null
            this.formatStats = {}
            this.clearMessages()
        },

        async uploadFile() {
            if (!this.selectedFile) return

            this.uploading = true
            this.uploadProgress = 0
            this.clearMessages()

            try {
                const formData = new FormData()
                formData.append('file', this.selectedFile)

                // 模拟上传进度
                const progressInterval = setInterval(() => {
                    if (this.uploadProgress < 90) {
                        this.uploadProgress += 10
                    }
                }, 200)

                const response = await uploadStratumData(formData, (progress) => {
                    this.uploadProgress = progress
                })

                clearInterval(progressInterval)
                this.uploadProgress = 100

                if (response.success) {
                    this.showMessage(response.message || '文件上传成功！', 'success')
                    
                    // 保存上传结果信息
                    this.uploadResult = {
                        filename: response.filename,
                        file_size: response.file_size,
                        file_type: response.file_type,
                        upload_time: response.upload_time,
                        file_path: response.file_path
                    }
                    
                    
                } else {
                    this.showMessage(response.message || '上传失败', 'error')
                }

            } catch (error) {
                this.showMessage('上传失败: ' + (error.message || '网络错误'), 'error')
            } finally {
                this.uploading = false
                setTimeout(() => {
                    this.uploadProgress = 0
                }, 2000)
            }
        },

        formatFileSize(bytes) {
            if (bytes === 0) return '0 Bytes'
            const k = 1024
            const sizes = ['Bytes', 'KB', 'MB', 'GB']
            const i = Math.floor(Math.log(bytes) / Math.log(k))
            return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i]
        },

        getFileType(filename) {
            const ext = filename.split('.').pop().toLowerCase()
            const types = {
                'txt': '地层坐标文件',
                'xlsx': 'Excel 工作簿',
                'xls': 'Excel 97-2003',
                'csv': 'CSV 文件'
            }
            return types[ext] || '未知格式'
        },

        getFileTypeDisplay(fileType) {
            // fileType 是后端返回的文件扩展名，如 '.txt', '.xlsx'
            const ext = fileType.replace('.', '').toLowerCase()
            const types = {
                'txt': '地层坐标文件 (.txt)',
                'xlsx': 'Excel 工作簿 (.xlsx)',
                'xls': 'Excel 97-2003 (.xls)',
                'csv': 'CSV 文件 (.csv)'
            }
            return types[ext] || `${fileType.toUpperCase()} 文件`
        },

        formatDateTime(isoString) {
            if (!isoString) return '-'
            try {
                const date = new Date(isoString)
                return date.toLocaleString('zh-CN', {
                    year: 'numeric',
                    month: '2-digit',
                    day: '2-digit',
                    hour: '2-digit',
                    minute: '2-digit',
                    second: '2-digit'
                })
            } catch (e) {
                return isoString
            }
        },

        showMessage(message, status) {
            this.uploadMessage = message
            this.uploadStatus = status
            
            if (status === 'success') {
                setTimeout(() => {
                    // 只清除消息，保留上传结果信息
                    this.uploadMessage = ''
                    this.uploadStatus = ''
                }, 5000)
            }
        },

        clearMessages() {
            this.uploadMessage = ''
            this.uploadStatus = ''
            this.uploadResult = null
        }
    }
}
</script>

